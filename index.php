                           <section> <p>Kartu Pas Bandara adalah tanda izin masuk daerah terbatas pada area Bandar Udara. Kartu Pas diterbitkan oleh Kantor Otoritas Wilayah pada masing-masing Bandara. Dalam hal ini untuk Bandara Soekarno Hatta adalah Kantor Otoritas Wilayah I.</p></section> 

<p><strong>1.NPA (Non Public Area)</strong></p>
<p>- Apron/Platform adalah suatu daerah atau tempat yang telah ditentukan guna menempatkan pesawat udara, menurunkan dan menaikkan penumpang, kargo, pos, pengisian bahan bakar dan parkir.<br>
- Fasilitas Vital yaitu Gedung Tower, Gedung Operasi Lalu Lintas Penerbangan, Gedung Radar, Gedung Listrik, Gedung Pemancar dan Penerima, Gedung PKP-PK, Gedung Meteorologi, Landasan, Taxi Way dan Peralatan Penunjang Navigasi Penerbangan<br>
- Gudang Kargo yaitu Gudang Kargo Domestik dan Internasional<br>
- Ruang tunggu keberangkatan, daerah imigrasi, daerah pabean, daerah karantina<br>
- Daerah kedatangan internasional</p>

<p>&nbsp;</p>

<p><strong>2.RPA (Restrected Public Area)</strong></p>
<p>Daerah Terbatas adalah bagian dari area bandar udara yang ditetapkan bukan sebagai daerah umum (NPA / Non Public Area) dan area bandar udara yang ditetapkan sebagai daerah umum terbatas (RPA / Restricted Public Area).</p>
<p>- Daerah Check In<br>
- Pelataran parkir Gudang Kargo Domestik dan Internasional<br>
- Gedung Catering dan DPPU Pertamina</p>

<p>Setiap kartu pas yang diterbitkan mempunyai kode yang menunjukkan area kerja dan masa berlaku kartu pas tersebut.Masa berlaku kartu PAS adalah minggun,bulanan,3 bulanan dan tahunan.Setiap habis masa berlakunya,maka kartu PAS bisa diperpanjang lagi.Pemohon kartu PAS pemula biasanya hanya diterbitkan kartu pas dengan masa berlaku yang pendek.Setelah masa kartu PAS berakhir baru diperbolehkan untuk mengajukan permohonan kartu pas dengan masa berlaku lebih panjang.Dari kartu pas mingguan,kemudian bulanan,3 bulanan dan setelah berakhir kartu pas 3 bulanan baru diperbolehkan untuk mengajukan permohonan kartu pas tahunan.</p>

<p>Area dimana kartu pas itu bisa digunakan direpresentasikan dalam kode-kode.Kode area dalam kartu PAS menunjukkan dimana si pemegang kartu PAS boleh masuk di sebuah area tertentu dan dimana si pemegang kartu PAS dilarang masuk.<br>
Kode area dalam kartu PAS adalah sebagai berikut :<br>
<br>
P = Platform<br>
A = Arrival Hall<br>
B = Boarding Lounge<br>
C = Chek In Counter<br>
S = Shopping Arcade<br>
T = Tower<br>
G = Gudang ( bagian dalam )<br>
F = Gudang ( bagian luar / halaman gudang )<br>
Ain = Arrival International<br>
V = Proyek Vital</p>

<p>&nbsp;</p>

<p>Persyaratan pembuatan kartu PAS Bandara :<br>
1.Kontrak Kerja ( dasar hukum pemohon untuk masuk ke wilayah bandara )<br>
2.Surat permohonan dari atasan tertinggi<br>
3.Surat Keterangan catatan Kepolisian ( SKCK )<br>
4.Surat Perjanjian Kerja Waktu tertentu ( PKWT ) bagi karyawan kontrak,surat penetapan karyawan bagi karyawan tetap.<br>
5.Daftar Riwayat Hidup<br>
6.Surat Pernyataan<br>
7.ID Card atau Surat Pengganti ID Card<br>
8. Foto Kopi KTP<br>
<br>
Setelah syarat-syarat dilengkapi dan permohonan diajukan, pihak Otoritas Bandara akan mengadakan screening bagi pemohon kartu PAS Bandara.</p>
                                
